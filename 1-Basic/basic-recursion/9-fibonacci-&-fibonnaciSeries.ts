function fib(n: number) {
  if (n <= 1) return n;
  return fib(n - 2) + fib(n - 1);
}

// Time: O(2^n)
// Space: O(n)

//  for series

function fibSeries(n: number): number[] {
  const outputArr: number[] = new Array(n).fill(0);
  outputArr[0] = 1;
  outputArr[1] = 1;

  for (let i = 2; i < outputArr.length; i++) {
    outputArr[i] = outputArr[i - 2] + outputArr[i - 1];
  }

  return outputArr;
}
// Time: O(n)
// Space: O(n)

